"""
Basic Math
'+' --> 'Addition'
'-' --> 'Subtraction'
'*' --> 'Multiplication'
'/' --> 'Division'
'**' --> 'Exponentiation'
'%' --> 'Modulo'
'//' --> 'Integer Division'
"""
num1 = 150
num2 = 29
add =  num1 + num2
sub = num1 - num2
mult = num1 * num2
div = num1 / num2
exponent = num1 ** num2
mod = num1 % num2
int_div = num1 // num2

print(f"Addition: {add}\nSubtraction: {sub}\nMultiplication: {mult}\nDivision: {div}\nExponentiation: {exponent}\nModulo: {mod}\nInteger Division: {int_div}")
