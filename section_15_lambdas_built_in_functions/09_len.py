# len() Function

"""
len():
    --> Return the length(the number of items) of an object. The argument may be a sequence(such as a string, tuple, list or range) or a collection (such as a dictionary, set).
"""
print(len([1, 2, 5, 'joker']))
print(len((5, 6, 'koras')))
print(len({ "a": 1, "b": 2 }))
print(len('Abhishek'))