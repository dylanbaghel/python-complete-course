# Type Conversion

"""
Type Conversion:
    --> String To Number {Only Works If String Contains The Number}
    --> Number To String
    --> Float to Int and Vice-Versa
    --> Boolean To String
    --> NoneType To String
    --> Boolean To Integer {0 -> False, 1 -> True}
"""
print(f"Number: {55} to String: '{str(55)}'")
print(f"String: '55' to Integer: {int('55')}")
print(f"Integer: {555} to Float: {float(555)}")
print(f"Float: {5546.4545} to Integer: {int(5546.4545)}")
print(f"Boolean: {True} to String: '{str(True)}''")
print(f"None: {None} to String: '{str(None)}'")
print(f"Boolean: {False} to Integer: {int(True)}")