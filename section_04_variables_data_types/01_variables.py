# Variables

"""
Variables --> A Variable in Python is like a Variable in mathematics: it is a named symbol that holds a value

Naming Restrictions:
    --> Variable Must Start With a letter or underscore.
    --> The rest of the name must consist of letters, numbers, or underscores.
    --> Names are case-sensitive

Naming Conventions:
    --> Multiword Variables Names should be 'snake_case'
    --> Capital 'SNAKE_CASE' usually refers to constants
    --> UpperCamelCase usually refers to classes
    --> Variables that start and end with two underscores (called "dunder" for double underscore are supposed to be private or left alone)
"""

# names2 - Valid
# 2names - Invalid
# _names - Valid
# names$ - Invalid
# names != Names -> Case-Sensitive

age = 21 # Here 'age' is a variable.
print(age)

age = 30 # Variables Can be reassigned
print(age)
