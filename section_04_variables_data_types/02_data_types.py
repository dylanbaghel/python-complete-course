# Data Types

"""
Standard Data Types:
    --> Boolean
    --> Numbers
    --> String
    --> List
    --> Tuple
    --> Dictionary
    --> NoneType
"""

isOpen = False
age = 21
name = 'Abhishek Baghel'
names = ['Abhishek', 'Dylan', 'Kora']
data = (55, 66)
d = {
    'name': 'Abhishek',
    'age': 21
}
print(type(isOpen))
print(type(age))
print(type(name))
print(type(names))
print(type(data))
print(type(d))
print(type(None))

"""
Python is highly flexible about reassigning variables to different types:
    --> This nature of language is called 'Dynamic Typing'

Java, C++ etc. are 'Static Typed Languages'.
"""